#!/usr/bin/env node
/**
 * Reads contracts/deployments/current.json (single source of truth)
 * and writes typed address files into every consumer:
 *   - landing/lib/addresses.generated.ts
 *   - dashboard/lib/addresses.generated.ts
 *
 * Run from repo root:   node scripts/sync-addresses.mjs
 * Run after redeploy:   edit current.json → run this script → commit
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');

const canonicalPath = path.join(repoRoot, 'contracts/deployments/current.json');
if (!fs.existsSync(canonicalPath)) {
  console.error(`❌ Missing ${canonicalPath}`);
  process.exit(1);
}

const canonical = JSON.parse(fs.readFileSync(canonicalPath, 'utf8'));
const { testnet, local } = canonical.networks;

function emit() {
  return `// AUTO-GENERATED from contracts/deployments/current.json
// Do not edit this file manually. Run: node scripts/sync-addresses.mjs
// Canonical source: contracts/deployments/current.json

export type NetworkName = 'local' | 'testnet';

export const DEPLOYMENTS = {
  testnet: {
    name: ${JSON.stringify(testnet.name)},
    chainId: ${testnet.chainId},
    rpc: ${JSON.stringify(testnet.rpc)},
    explorer: ${JSON.stringify(testnet.explorer)},
    addresses: ${JSON.stringify(testnet.addresses, null, 6).replace(/\n/g, '\n    ')} as const,
  },
  local: {
    name: ${JSON.stringify(local.name)},
    chainId: ${local.chainId},
    rpc: ${JSON.stringify(local.rpc)},
    explorer: ${JSON.stringify(local.explorer)},
    addresses: ${JSON.stringify(local.addresses, null, 6).replace(/\n/g, '\n    ')} as const,
  },
} as const;

export const DEPLOYMENT_VERSION = ${JSON.stringify(canonical.version)};
`;
}

const targets = [
  path.join(repoRoot, 'landing/lib/addresses.generated.ts'),
  path.join(repoRoot, 'dashboard/lib/addresses.generated.ts'),
];

const content = emit();
for (const t of targets) {
  const dir = path.dirname(t);
  if (!fs.existsSync(dir)) {
    console.warn(`⚠️  ${dir} doesn't exist, skipping`);
    continue;
  }
  fs.writeFileSync(t, content);
  console.log(`✓ Wrote ${path.relative(repoRoot, t)}`);
}

console.log('\nAddress sync complete.');
console.log(`Testnet: ${testnet.chainId} · ${Object.keys(testnet.addresses).length} contracts`);
console.log(`Local:   ${local.chainId}    · ${Object.keys(local.addresses).length} contracts`);
